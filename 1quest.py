score = float(input("Введіть кількість балів (0-100): "))

if 0 <= score <= 49:
    print("Результат: незадовільно")
elif 50 <= score <= 69:
    print("Результат: задовільно")
elif 70 <= score <= 89:
    print("Результат: добре")
elif 90 <= score <= 100:
    print("Результат: відмінно")
else:
    print("Помилка! Введіть значення від 0 до 100.")
